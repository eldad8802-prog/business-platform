type Guidance = {
  tip: string;
  example: string;
  insight: string;
};

type BusinessProfile = {
  category?: string;
  subCategory?: string;
  businessModel?: string;
} | null;

function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function normalize(value?: string | null): string {
  return (value || "").trim().toLowerCase();
}

function includesAny(text: string, keywords: string[]): boolean {
  return keywords.some((keyword) => text.includes(keyword));
}

function detectSubtype(profile: BusinessProfile): string {
  const category = normalize(profile?.category);
  const subCategory = normalize(profile?.subCategory);
  const model = normalize(profile?.businessModel);

  const combined = [category, subCategory, model].join(" ");

  if (
    includesAny(combined, [
      "barber",
      "hair",
      "fade",
      "מספר",
      "תספורת",
      "שיער",
    ])
  ) {
    return "BARBER";
  }

  if (
    includesAny(combined, [
      "beauty",
      "cosmetic",
      "cosmetician",
      "facial",
      "skin",
      "lashes",
      "brows",
      "קוסמט",
      "טיפוח",
      "טיפול פנים",
      "פנים",
      "יופי",
    ])
  ) {
    return "COSMETICS";
  }

  if (
    includesAny(combined, [
      "clinic",
      "therapy",
      "treatment",
      "wellness",
      "massage",
      "physio",
      "מטפל",
      "מטפלת",
      "קליניק",
      "טיפול",
      "עיסוי",
      "פיזיו",
      "רפואה משלימה",
    ])
  ) {
    return "CLINIC";
  }

  if (
    includesAny(combined, [
      "restaurant",
      "food",
      "kitchen",
      "chef",
      "מסעד",
      "אוכל",
      "מטבח",
    ])
  ) {
    return "RESTAURANT";
  }

  if (
    includesAny(combined, [
      "cafe",
      "coffee",
      "bakery",
      "pastry",
      "בית קפה",
      "קפה",
      "מאפיה",
    ])
  ) {
    return "CAFE";
  }

  if (
    includesAny(combined, [
      "hotel",
      "hospitality",
      "zimmer",
      "guesthouse",
      "bnb",
      "מלון",
      "צימר",
      "אירוח",
    ])
  ) {
    return "HOSPITALITY";
  }

  if (
    includesAny(combined, [
      "gym",
      "fitness",
      "studio",
      "pilates",
      "yoga",
      "חדר כושר",
      "כושר",
      "אימון",
      "פילאטיס",
      "יוגה",
      "סטודיו",
    ])
  ) {
    return "FITNESS";
  }

  if (
    includesAny(combined, [
      "fashion",
      "clothing",
      "clothes",
      "boutique",
      "apparel",
      "אופנה",
      "בגדים",
      "בוטיק",
    ])
  ) {
    return "FASHION_STORE";
  }

  if (
    includesAny(combined, [
      "electronics",
      "electronic",
      "mobile",
      "computer",
      "חשמל",
      "אלקטרונ",
      "סלולר",
      "מחשבים",
      "טלפונים",
    ])
  ) {
    return "ELECTRONICS_STORE";
  }

  if (
    includesAny(combined, [
      "shop",
      "store",
      "retail",
      "חנות",
      "קמעונאות",
      "מוצרים",
    ])
  ) {
    return "GENERAL_RETAIL";
  }

  if (
    includesAny(combined, [
      "doctor",
      "dentist",
      "medical",
      "רופא",
      "רופאה",
      "רופא שיניים",
      "רפואי",
    ])
  ) {
    return "MEDICAL_APPOINTMENT";
  }

  if (
    includesAny(combined, [
      "consultant",
      "coach",
      "advisor",
      "consulting",
      "יועץ",
      "יועצת",
      "מאמן",
      "מאמנת",
      "ייעוץ",
    ])
  ) {
    return "CONSULTING";
  }

  if (
    includesAny(combined, [
      "technician",
      "repair",
      "installer",
      "electrician",
      "plumber",
      "air conditioning",
      "טכנאי",
      "טכנאית",
      "תיקונים",
      "חשמלאי",
      "אינסטלטור",
      "מזגנים",
      "מיזוג",
      "התקנות",
    ])
  ) {
    return "FIELD_SERVICE";
  }

  if (
    includesAny(combined, [
      "course",
      "digital",
      "saas",
      "software",
      "online",
      "membership",
      "subscription",
      "קורס",
      "דיגיטל",
      "תוכנה",
      "אונליין",
      "מנוי",
      "מוצר דיגיטלי",
    ])
  ) {
    return "DIGITAL";
  }

  if (model === "product") return "GENERAL_RETAIL";
  if (model === "service") return "GENERAL_SERVICE";
  if (model === "hybrid") return "HYBRID";

  return "GENERAL";
}

