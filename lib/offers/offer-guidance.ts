export function getOfferGuidance(profile: BusinessProfile): Guidance {
  const subtype = detectSubtype(profile);

  switch (subtype) {
    case "BARBER":
    case "COSMETICS":
    case "CLINIC":
    case "GENERAL_SERVICE":
      return pickRandom(SERVICE_BASED_GUIDANCE);

    case "FASHION_STORE":
    case "ELECTRONICS_STORE":
    case "GENERAL_RETAIL":
      return pickRandom(PRODUCT_BASED_GUIDANCE);

    case "RESTAURANT":
    case "CAFE":
    case "HOSPITALITY":
    case "FITNESS":
      return pickRandom(CAPACITY_BASED_GUIDANCE);

    case "MEDICAL_APPOINTMENT":
    case "CONSULTING":
      return pickRandom(APPOINTMENT_BASED_GUIDANCE);

    case "FIELD_SERVICE":
      return pickRandom(FIELD_SERVICE_GUIDANCE);

    case "DIGITAL":
      return pickRandom(DIGITAL_GUIDANCE);

    case "HYBRID":
      return pickRandom(HYBRID_GUIDANCE);

    default:
      return pickRandom(GENERAL_GUIDANCE);
  }
}